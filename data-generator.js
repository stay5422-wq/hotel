function generatePhone(city, index) {
    const code = CITY_PHONE_CODES[city] || '011';
    const baseNum = 2000000 + (index * 1000);
    return `0${code}${baseNum}`;
}

function generateData(type, count = 50) {
    const data = [];
    let id = 1;
    let namesArray;
    
    switch(type) {
        case 'hotels': namesArray = HOTEL_NAMES; break;
        case 'apartments': namesArray = APARTMENT_NAMES; break;
        case 'resorts': namesArray = RESORT_NAMES; break;
        case 'caravans': namesArray = CARAVAN_NAMES; break;
        default: namesArray = HOTEL_NAMES;
    }
    
    CITIES.forEach(city => {
        for (let i = 0; i < count; i++) {
            const name = namesArray[i % namesArray.length];
            const neighborhood = NEIGHBORHOODS[i % NEIGHBORHOODS.length];
            let status;
            
            if (i < 15) status = 'تم التعاقد';
            else if (i < 30) status = 'جاري التعاقد';
            else if (i < 45) status = 'غير متعاقد';
            else status = 'رفض التعاقد';
            
            data.push({
                id: id++,
                name: type === 'hotels' ? `${name}` : `${name}`,
                address: `${neighborhood}، ${city}`,
                city: city,
                phone: generatePhone(city, i),
                status: status
            });
        }
    });
    
    return data;
}

